import * as SecureStore from 'expo-secure-store';

// Register user
export const signUp = async (username, phoneNumber, password) => {
  const url = 'https://ums.granularx.com/auth/signup';
 
  try {
    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        username,
        phone_number: phoneNumber,
        password,
      }),
    });
    const data = await response.json();
    if (response.status === 201) {
      return data;
    } else {
      throw new Error(data.error || 'Signup failed');
    }
  } catch (error) {
    console.error('Signup error:', error);
    throw error;
  }
};

// Sign In
export const signIn = async (username, password) => {
  const baseUrl = 'https://ums.granularx.com/auth/signin';
  const url = `${baseUrl}?platform=mobile`; // Always use mobile platform

  try {
    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        username,
        password,
      }),
    });

    const data = await response.json();

    if (response.status === 200) {
      // Successful sign-in
      if (data.access_Token && data.refresh_Token) {
        // Store tokens securely
        await SecureStore.setItemAsync('accessToken', data.access_Token);
        await SecureStore.setItemAsync('refreshToken', data.refresh_Token);
      }
      return data; // This should include user data and any other relevant information
    } else {
      // Sign-in failed
      throw new Error(data.error || 'Sign-in failed');
    }
  } catch (error) {
    throw error;
  }
};

